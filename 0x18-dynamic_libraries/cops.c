#include <stdlib.h>
#include <stdio.h>

/**
 * add - adds two numbers
 * @a: first unit
 * @b:second argument
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two numbers
 * @a: first unit
 * @b:second argument
 * Return: subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a: first unit
 * @b:second argument
 * Return: multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * mydiv - divides two numbers
 * @a: first unit
 * @b:second argument
 * Return: division
 */
int mydiv(int a, int b)
{
	return (a / b);
}

/**
 * mod - modular, checks remainder of two numbers
 * @a: first unit
 * @b:second argument
 * Return: remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
